LITKE_519_120UM_ARRAY_ID_CARVEOUT = {
    1505,
    1506,
    1510,
    1601
}


def determine_array_type(array_id: int) -> int:
    # from George, modified Vision ElectrodeMapFactory.java:
    # the 120 um arrays have array id 3500 <= array_id < 4000
    # or have specific array id 1505, 1506

    array_id = array_id & 0xFFF

    if 1500 <= array_id < 2500:
        if array_id in LITKE_519_120UM_ARRAY_ID_CARVEOUT:
            return LitkeArrayType.LITKE_519_ARRAY_120UM
        else:
            return LitkeArrayType.LITKE_519_ARRAY_30UM
    elif 500 <= array_id < 1500:
        return LitkeArrayType.LITKE_512_ARRAY
    elif 3500 <= array_id < 4000:
        return LitkeArrayType.LITKE_519_ARRAY_120UM
    else:
        assert False, "No such Litke array id {0}".format(array_id)


def generate_120um_519_map_from_30um_519_map():
    return 4 * LITKE_519_ARRAY_MAP


LITKE_519_ARRAY_MAP_120UM = generate_120um_519_map_from_30um_519_map()


def get_litke_array_coordinates_by_array_id(array_id):
    array_type = determine_array_type(array_id)
    if array_type == LitkeArrayType.LITKE_519_ARRAY_120UM:
        return LITKE_519_ARRAY_MAP_120UM
    elif array_type == LitkeArrayType.LITKE_519_ARRAY_30UM:
        return LITKE_519_ARRAY_MAP
    elif array_type == LitkeArrayType.LITKE_512_ARRAY:
        return LITKE_512_ARRAY_MAP
    else:
        assert False, "could not determine map for array id {0}".format(array_id)


def get_litke_array_adj_mat_by_array_id(array_id):
    '''
    NOTE: these are 0 indexed, to be consistent with other electrode map tools.
    '''

    array_type = determine_array_type(array_id)
    if array_type == LitkeArrayType.LITKE_519_ARRAY_120UM:
        return LITKE_519_ARRAY_ADJ_MAT
    elif array_type == LitkeArrayType.LITKE_519_ARRAY_30UM:
        return LITKE_519_ARRAY_ADJ_MAT
    elif array_type == LitkeArrayType.LITKE_512_ARRAY:
        return LITKE_512_ARRAY_ADJ_MAT
    else:
        assert False, "could not determine adj mat for array id {0}".format(array_id)


def get_disconnected_electrode_set_by_array_id(array_id):
    array_type = determine_array_type(array_id)
    if array_type == LitkeArrayType.LITKE_519_ARRAY_120UM or LitkeArrayType.LITKE_519_ARRAY_30UM:
        return {0, 129, 258, 259, 388, 389, 518}
    else:
        return set()


